package P18

import org.scalatest._

class TestP18 extends FlatSpec with Matchers {
  import P18._

  "slice of P18" should "slice the a list from start element to end element" in {
    slice(3, 7, List('a, 'b, 'c, 'd, 'e, 'f, 'g, 'h, 'i, 'j, 'k)) should be (List('d, 'e, 'f, 'g))
    sliceAnswer1(3, 7, List('a, 'b, 'c, 'd, 'e, 'f, 'g, 'h, 'i, 'j, 'k)) should be (List('d, 'e, 'f, 'g))
  }
}
